package sifmultitool;

/**
 *
 * @author Umidah
 */
public class CardCalculationResults {
    
    private int initialBaseStat;
    private int finalBaseStat;
    private int noteScore;
    private int skillScore;

    /**
     * @return the initialBaseStat
     */
    public int getInitialBaseStat() {
        return initialBaseStat;
    }

    /**
     * @param initialBaseStat the initialBaseStat to set
     */
    public void setInitialBaseStat(int initialBaseStat) {
        this.initialBaseStat = initialBaseStat;
    }

    /**
     * @return the finalBaseStat
     */
    public int getFinalBaseStat() {
        return finalBaseStat;
    }

    /**
     * @param finalBaseStat the finalBaseStat to set
     */
    public void setFinalBaseStat(int finalBaseStat) {
        this.finalBaseStat = finalBaseStat;
    }

    /**
     * @return the noteScore
     */
    public int getNoteScore() {
        return noteScore;
    }

    /**
     * @param noteScore the noteScore to set
     */
    public void setNoteScore(int noteScore) {
        this.noteScore = noteScore;
    }

    /**
     * @return the skillScore
     */
    public int getSkillScore() {
        return skillScore;
    }

    /**
     * @param skillScore the skillScore to set
     */
    public void setSkillScore(int skillScore) {
        this.skillScore = skillScore;
    }
    
}
